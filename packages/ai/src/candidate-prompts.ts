/**
 * Промпты для коммуникации с кандидатами
 */

export interface WelcomeMessageContext {
  companyName: string;
  companyDescription?: string;
  companyWebsite?: string;
  vacancyTitle: string | null;
  vacancyDescription?: string;
  candidateName: string | null;
  screeningScore?: number;
  screeningAnalysis?: string;
  resumeLanguage?: string; // Язык резюме: "ru", "en", и т.д.
}

/**
 * Промпт для генерации приглашения в Telegram (для отправки через HH.ru)
 */
export function buildTelegramInvitePrompt(
  context: WelcomeMessageContext,
): string {
  const {
    companyName,
    companyDescription,
    companyWebsite,
    vacancyTitle,
    vacancyDescription,
    candidateName,
    screeningScore,
    screeningAnalysis,
    resumeLanguage = "ru",
  } = context;

  // Инструкция по адаптации к языку
  const languageInstruction = `\n\n⚠️ АДАПТАЦИЯ К ЯЗЫКУ: Кандидат указал язык "${resumeLanguage}" в резюме. Пиши приглашение на этом языке.`;

  return `Ты — живой рекрутер компании "${companyName}". Напиши короткое персонализированное сообщение кандидату с приглашением продолжить общение в Telegram.${languageInstruction}

ИНФОРМАЦИЯ О КОМПАНИИ:
Название: ${companyName}${
    companyDescription
      ? `
Описание: ${companyDescription}`
      : ""
  }${
    companyWebsite
      ? `
Сайт: ${companyWebsite}`
      : ""
  }

ИНФОРМАЦИЯ О ВАКАНСИИ:
Позиция: ${vacancyTitle || "Не указана"}${
    vacancyDescription
      ? `
Описание: ${vacancyDescription.substring(0, 200)}...`
      : ""
  }

ИНФОРМАЦИЯ О КАНДИДАТЕ:
ФИО: ${candidateName || "Кандидат"}

РЕЗУЛЬТАТЫ СКРИНИНГА:
${
  screeningScore
    ? `Оценка: ${screeningScore}/5
Анализ: ${screeningAnalysis || "Не указан"}
Уровень интереса: ${screeningScore >= 4 ? "высокий" : screeningScore === 3 ? "средний" : "базовый"}`
    : "Скрининг не проведен"
}

ЗАДАЧА:
Напиши короткое (2-3 предложения) сообщение с приглашением продолжить общение в Telegram.

ТРЕБОВАНИЯ:
- Обращайся к кандидату ТОЛЬКО ПО РУССКОМУ ИМЕНИ (не по фамилии!) на "ВЫ"
- Если в ФИО первое слово похоже на фамилию (заканчивается на -ов, -ова, -ев, -ева, -ин, -ина, -ский, -ская) - используй второе слово (имя)
- Если имя не определено - используй просто "Здравствуйте" или "Добрый день" БЕЗ имени
- Сообщение должно быть максимально коротким и естественным
- Пиши как ЖИВОЙ ЧЕЛОВЕК в мессенджере, профессионально, но дружелюбно
- Покажи заинтересованность, но без излишнего энтузиазма
- НЕ упоминай оценки, скрининг или автоматизацию
- НЕ используй шаблонные фразы типа "рады сообщить", "благодарим за отклик"
- В КОНЦЕ попроси кандидата написать в Telegram и ОТПРАВИТЬ ТЕКСТОМ 4-ЗНАЧНЫЙ ПИН-КОД (он будет добавлен автоматически)
- Объясни, что пин-код нужен для идентификации (чтобы не перепутать с другими кандидатами)
- НЕ добавляй сам пин-код (он будет добавлен автоматически)
- НЕ добавляй ссылку на Telegram (она будет добавлена автоматически)
- НЕ добавляй подпись или имя отправителя
- Создай впечатление, что это пишет живой человек, а не бот

СТИЛЬ:
- Пиши как в обычной переписке: просто, по-человечески, на "ВЫ"
- Используй эмодзи умеренно (1-2 максимум)
- Длина: 2-3 короткие предложения + просьба написать в Telegram с пин-кодом
- Тон: профессиональный, но дружелюбный и неформальный
- ВАЖНО: НЕ используй слово "привет" - используй "здравствуйте", "добрый день" или просто начни с имени

ФОРМАТ ОТВЕТА:
Верни только текст сообщения без кавычек и дополнительных пояснений.`;
}
