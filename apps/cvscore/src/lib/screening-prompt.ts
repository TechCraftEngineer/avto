import { z } from "zod";

/** Мин. символов для резюме (валидация API) */
export const RESUME_MIN_CHARS = 50;

/** Макс. символов для резюме (чтобы уложиться в 10–20 сек) */
export const RESUME_MAX_CHARS = 15_000;

/** Мин. символов для вакансии (валидация API) */
export const VACANCY_MIN_CHARS = 30;

/** Макс. символов для вакансии */
export const VACANCY_MAX_CHARS = 5_000;

export const screeningOutputSchema = z.object({
  score: z.number().min(0).max(100),
  strengths: z.array(z.string().max(300)).min(1).max(8),
  risks: z.array(z.string().max(300)).min(0).max(8),
  interviewQuestions: z.array(z.string().max(400)).min(3).max(8),
});

export type ScreeningOutput = z.infer<typeof screeningOutputSchema>;

export function buildScreeningPrompt(
  resumeText: string,
  vacancyText: string,
): string {
  return `Ты опытный технический рекрутер. Твоя задача — тщательно проанализировать резюме кандидата и оценить соответствие вакансии.

═══════════════════════════════════════════════════════════════════════════════
ВАКАНСИЯ:
${vacancyText}

═══════════════════════════════════════════════════════════════════════════════
РЕЗЮМЕ КАНДИДАТА:
${resumeText}

═══════════════════════════════════════════════════════════════════════════════

КРИТИЧЕСКИ ВАЖНЫЕ ПРАВИЛА АНАЛИЗА:

1. ПОДСЧЕТ ОПЫТА:
   - СУММИРУЙ весь релевантный опыт из ВСЕХ мест работы
   - Если кандидат работал с технологией в нескольких компаниях — складывай периоды
   - Пример: Node.js в компании А (2015-2018) + Node.js в компании Б (2020-2023) = 6 лет опыта с Node.js
   - НЕ ограничивайся только текущим местом работы
   - Учитывай overlap (если технологии использовались параллельно, берем максимальный период)

2. ИЗВЛЕЧЕНИЕ НАВЫКОВ:
   - Ищи технологии в разделах: опыт работы, проекты, навыки, достижения
   - Учитывай синонимы: JS = JavaScript, TS = TypeScript, React.js = React
   - Обращай внимание на версии и уровень владения (junior/middle/senior)

3. ОЦЕНКА ТРЕБОВАНИЙ:
   - Различай hard requirements (must-have) и nice-to-have
   - Если в вакансии написано "3+ года опыта с X", а у кандидата 5 лет — это сильная сторона
   - Если требование не указано явно, но навык есть — это бонус

4. КОНТЕКСТ И ГЛУБИНА:
   - Оценивай не только наличие навыка, но и глубину (pet-project vs production на миллионы пользователей)
   - Учитывай масштаб проектов, размер команды, ответственность
   - Обращай внимание на лидерские качества, менторство, архитектурные решения

═══════════════════════════════════════════════════════════════════════════════

ШКАЛА ОЦЕНКИ (score: 0–100):

0–30   | НЕ ПОДХОДИТ
       | Критические несоответствия ключевым требованиям
       | Отсутствуют must-have навыки или опыт в 2+ раза меньше требуемого

31–50  | СЛАБОЕ СООТВЕТСТВИЕ  
       | Есть базовые навыки, но значительные пробелы в ключевых требованиях
       | Опыт есть, но недостаточный или не релевантный

51–70  | СРЕДНЕЕ СООТВЕТСТВИЕ
       | Покрывает большинство основных требований
       | Есть риски или пробелы в некоторых областях, но можно рассмотреть

71–85  | ХОРОШЕЕ СООТВЕТСТВИЕ
       | Соответствует всем или почти всем ключевым требованиям
       | Минимальные риски, кандидат может справиться с задачами

86–100 | ОТЛИЧНОЕ СООТВЕТСТВИЕ
       | Полностью покрывает все требования + есть дополнительные преимущества
       | Превосходит ожидания по опыту, навыкам или достижениям

═══════════════════════════════════════════════════════════════════════════════

СТРУКТУРА ОТВЕТА:

1. score (число 0–100)
   Итоговая оценка соответствия по шкале выше

2. strengths (массив из 1–8 строк)
   Конкретные сильные стороны с привязкой к требованиям:
   
   ✓ Формат: "[Факт из резюме] — [связь с требованием вакансии]"
   ✓ Примеры:
     • "10 лет коммерческого опыта с Node.js — значительно превышает требование 5+ лет"
     • "Опыт архитектуры микросервисов на 3 проектах — прямое соответствие требованию"
     • "Руководил командой из 12 разработчиков — подходит для позиции тимлида"
   
   ✓ Указывай СУММАРНЫЙ опыт, а не только с последнего места работы
   ✓ Включай измеримые достижения (метрики, масштаб, результаты)
   ✓ Отмечай редкие или особо ценные навыки

3. risks (массив из 0–8 строк)
   Риски, пробелы и моменты, требующие уточнения:
   
   ✓ Несоответствия must-have требованиям:
     • "Нет опыта с Kubernetes, указанным как обязательное требование"
   
   ✓ Неясности и пробелы:
     • "Не указан уровень владения Python — требуется уточнение"
     • "Перерыв в работе с июня по декабрь 2023 — причина не указана"
   
   ✓ Потенциальные красные флаги:
     • "4 смены работы за последние 2 года — возможны проблемы с удержанием"
     • "Все проекты длились менее 6 месяцев — нет опыта долгосрочной поддержки"
   
   ✓ Если рисков нет или они минимальны — оставь массив пустым []

4. interviewQuestions (массив из 3–8 строк)
   Вопросы для проверки ключевых компетенций и прояснения рисков:
   
   ✓ Открытые вопросы для проверки глубины навыков:
     • "Расскажите о самой сложной проблеме производительности, которую вы решили в Node.js. Какой был подход?"
     • "Опишите архитектуру самого масштабного проекта, в котором вы участвовали. Какие решения принимали?"
   
   ✓ Вопросы для прояснения рисков:
     • "Вы указали опыт с GraphQL, но не описали проекты. Расскажите подробнее о применении"
     • "Почему вы сменили 3 компании за последний год?"
   
   ✓ Ситуационные вопросы для soft skills:
     • "Как вы разрешаете конфликты в команде при разногласиях по техническим решениям?"
     • "Приведите пример, когда вам пришлось обучать junior-разработчика. Как строили процесс?"
   
   ✓ ИЗБЕГАЙ вопросов с ответом да/нет
   ✓ Фокусируйся на проверке критичных для вакансии навыков

═══════════════════════════════════════════════════════════════════════════════

ФИНАЛЬНЫЕ ТРЕБОВАНИЯ:
- Будь объективным и точным в подсчете опыта
- Каждое утверждение должно быть основано на фактах из резюме
- Пиши понятным языком для рекрутеров без глубокого технического бэкграунда
- Используй только русский язык
- Будь конкретным: вместо "хороший опыт" пиши "7 лет опыта с React"`;
}
